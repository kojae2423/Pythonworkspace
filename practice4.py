##숫자처리함수##

print(abs(-5)) #절댓값 5
print(pow(4, 2)) # 4^2 = 16 거듭제곱
print(max(5, 12)) # 12 최댓값
print(min(5, 12)) # 5 최솟값
print(round(3.14)) # 3 반올림
print(round(4.999)) # 5

from math import * # 나중에 배울것. 
                   # math 라이브러리에 있는 모든것을 쓰겠다.
 
print(floor(4.99)) # 내림. 4
print(ceil(3.14)) # 올림. 4
print(sqrt(16)) # 제곱근. 4